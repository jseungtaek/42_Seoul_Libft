#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*res;
	unsigned int	len;
	unsigned int	i;

	if (s == 0 || f == 0)
		return (0);
	len = ft_strlen(s);
	i = 0;
	res = (char)malloc(sizeof(char) * len + 1);
	if (res == 0)
		return (0);
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
